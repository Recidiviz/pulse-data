def file_tag_exempt_from_automatic_raw_data_pruning(
    state_code: StateCode, raw_data_instance: DirectIngestInstance, file_tag: str
) -> bool:
    """Returns True if the given file tag is exempt from automatic raw data pruning for the given state code."""
    if raw_data_instance == DirectIngestInstance.SECONDARY:
        return False
    return (
        state_code in FILES_EXEMPT_FROM_AUTOMATIC_RAW_DATA_PRUNING_BY_STATE
        and file_tag
        in FILES_EXEMPT_FROM_AUTOMATIC_RAW_DATA_PRUNING_BY_STATE[state_code]
    )
