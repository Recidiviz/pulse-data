"""Ingest view for person information for the hunger games in US_OZ."""

from recidiviz.ingest.direct.views.direct_ingest_view_query_builder import (
    DirectIngestViewQueryBuilder,
)
from recidiviz.utils.environment import GCP_PROJECT_STAGING
from recidiviz.utils.metadata import local_project_id_override

VIEW_QUERY_TEMPLATE = """
SELECT 
    hg_people.id,
    hg_people.first_name,
    hg_people.last_name,
    hg_background.district,
    hg_background.skill
FROM {hunger_games_people} AS hg_people
LEFT JOIN {hunger_games_background} AS hg_background
ON
    hg_people.id = hg_background.id
"""

VIEW_BUILDER = DirectIngestViewQueryBuilder(
    region="us_oz",
    ingest_view_name="hunger_games_people",
    view_query_template=VIEW_QUERY_TEMPLATE,
)

if __name__ == "__main__":
    with local_project_id_override(GCP_PROJECT_STAGING):
        VIEW_BUILDER.build_and_print()
