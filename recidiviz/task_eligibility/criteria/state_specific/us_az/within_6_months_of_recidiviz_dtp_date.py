"""Describes spans of time when someone is within 6 months of their Recidiviz-projected DTP Date"""
from google.cloud import bigquery

from recidiviz.calculator.query.state.dataset_config import ANALYST_VIEWS_DATASET
from recidiviz.common.constants.states import StateCode
from recidiviz.task_eligibility.reasons_field import ReasonsField
from recidiviz.task_eligibility.task_criteria_big_query_view_builder import (
    StateSpecificTaskCriteriaBigQueryViewBuilder,
)
from recidiviz.task_eligibility.utils.us_az_query_fragments import (
    within_x_months_of_date,
)
from recidiviz.utils.environment import GCP_PROJECT_STAGING
from recidiviz.utils.metadata import local_project_id_override

_CRITERIA_NAME = "US_AZ_WITHIN_6_MONTHS_OF_RECIDIVIZ_DTP_DATE"

_REASONS_FIELDS = [
    ReasonsField(
        name="recidiviz_dtp_date",
        type=bigquery.enums.StandardSqlTypeNames.DATE,
        description="Projected Recidiviz DTP eligibility date",
    ),
]

VIEW_BUILDER: StateSpecificTaskCriteriaBigQueryViewBuilder = (
    StateSpecificTaskCriteriaBigQueryViewBuilder(
        criteria_name=_CRITERIA_NAME,
        description=__doc__,
        criteria_spans_query_template=within_x_months_of_date("DTP", time_interval=6),
        reasons_fields=_REASONS_FIELDS,
        state_code=StateCode.US_AZ,
        analyst_views_dataset=ANALYST_VIEWS_DATASET,
    )
)

if __name__ == "__main__":
    with local_project_id_override(GCP_PROJECT_STAGING):
        VIEW_BUILDER.build_and_print()
