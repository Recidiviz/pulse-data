"""Generates BQ source table YAML files for the document extraction eval dataset.

Each file defines a Google-Sheet-backed BigQuery external table whose schema is
derived automatically from the collection's golden_eval_config.yaml (which in turn
derives columns from collection.yaml).  Run this script whenever a collection's
output schema or golden_eval_config.yaml changes:

    python -m recidiviz.NOT_FOR_PRODUCTION_USE.source_tables.generate_eval_source_table_yamls

Output: recidiviz/NOT_FOR_PRODUCTION_USE/source_tables/yaml_managed/
            document_extraction_collection_golden_eval_data/{collection_name_lower}.yaml
"""
import os
import re
import textwrap

import recidiviz.NOT_FOR_PRODUCTION_USE.source_tables as _nfpu_source_tables
from recidiviz.big_query.big_query_view_column import BigQueryViewColumn
from recidiviz.NOT_FOR_PRODUCTION_USE.documents.extraction.document_extractor_configs import (
    collect_extractor_collection_eval_configs,
)
from recidiviz.NOT_FOR_PRODUCTION_USE.documents.extraction.eval.golden_eval_config import (
    GoldenEvalConfig,
)

_OUTPUT_DIR = os.path.join(
    os.path.dirname(_nfpu_source_tables.__file__),
    "yaml_managed",
    "document_extraction_collection_golden_eval_data",
)

_EVAL_DATASET_ID = "document_extraction_collection_golden_eval_data"


def _sheet_id_from_source_uri(source_uri: str) -> str:
    match = re.search(r"/spreadsheets/d/([^/]+)", source_uri)
    if not match:
        raise ValueError(f"Cannot extract sheet ID from source_uri: {source_uri!r}")
    return match.group(1)


def _render_column(col: BigQueryViewColumn, indent: str = "  ") -> str:
    bq_type = col.field_type.name
    description = col.description

    # Block scalar content must be indented further than the key itself.
    # The key sits at len(indent)+2 spaces; content needs at least 2 more.
    content_indent = indent + "    "
    wrapped = textwrap.fill(description, width=72 - len(content_indent))
    if "\n" in wrapped:
        desc_yaml = ">\n" + textwrap.indent(wrapped + "\n", content_indent)
    else:
        desc_yaml = f'"{description}"'

    return (
        f"{indent}- name: {col.name}\n"
        f"{indent}  type: {bq_type}\n"
        f"{indent}  mode: {col.mode}\n"
        f"{indent}  description: {desc_yaml}"
    )


def generate_eval_source_table_yaml(
    collection_name: str,
    eval_schema: GoldenEvalConfig,
) -> str:
    """Renders the BQ source table YAML content for one eval collection."""
    table_id = collection_name.lower()
    sheet_id = _sheet_id_from_source_uri(eval_schema.source_uri)
    schema_lines = "\n".join(_render_column(col) for col in eval_schema.columns)

    return f"""\
# Auto-generated by generate_eval_source_table_yamls.py. Do not edit manually.
# Re-run that script after any schema change.
address:
  dataset_id: {_EVAL_DATASET_ID}
  table_id: {table_id}

description: >
  Golden eval set for {collection_name.upper()}. One row per labeled document.
  Backed by a Google Sheet (one tab per collection). Both synthetic and real
  (PII) documents can live here — the sheet is access-controlled. PII must
  never be committed to the repo.

schema:
{schema_lines}

clustering_fields: null

external_data_configuration:
  sourceUris:
    - https://docs.google.com/spreadsheets/d/{sheet_id}
  sourceFormat: GOOGLE_SHEETS
  autodetect: false
  ignoreUnknownValues: true
  googleSheetsOptions:
    skipLeadingRows: "1"
    range: "{table_id}"
"""


def main() -> None:
    eval_configs = collect_extractor_collection_eval_configs()
    generated = []

    for collection_name, eval_schema in sorted(eval_configs.items()):
        content = generate_eval_source_table_yaml(
            collection_name=collection_name,
            eval_schema=eval_schema,
        )

        out_path = os.path.join(_OUTPUT_DIR, f"{collection_name.lower()}.yaml")
        with open(out_path, "w", encoding="utf-8") as f:
            f.write(content)
        generated.append(out_path)
        print(f"Wrote {out_path}")

    if not generated:
        print("No golden_eval_config.yaml files found — nothing generated.")


if __name__ == "__main__":
    main()
