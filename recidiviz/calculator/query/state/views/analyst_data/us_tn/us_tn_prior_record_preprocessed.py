"""View of prior record sentences in TN"""

from recidiviz.big_query.big_query_view import SimpleBigQueryViewBuilder
from recidiviz.calculator.query.state.dataset_config import (
    ANALYST_VIEWS_DATASET,
    NORMALIZED_STATE_DATASET,
)
from recidiviz.common.constants.states import StateCode
from recidiviz.ingest.direct.dataset_config import raw_latest_views_dataset_for_region
from recidiviz.ingest.direct.types.direct_ingest_instance import DirectIngestInstance
from recidiviz.utils.environment import GCP_PROJECT_STAGING
from recidiviz.utils.metadata import local_project_id_override

US_TN_PRIOR_RECORD_PREPROCESSED_VIEW_NAME = "us_tn_prior_record_preprocessed"

US_TN_PRIOR_RECORD_PREPROCESSED_VIEW_DESCRIPTION = (
    """View of prior record sentences in TN"""
)

US_TN_PRIOR_RECORD_PREPROCESSED_QUERY_TEMPLATE = """
    WITH offense_flag_lk_cte AS
    (
    SELECT
        REGEXP_REPLACE(OffenseDescription, "  ", " ") AS description,
        LOGICAL_OR(AssaultiveOffenseFlag = 'Y') AS is_violent,
        LOGICAL_OR(SexOffenderFlag = 'Y') AS is_sex_offense
    FROM `{project_id}.{raw_data_up_to_date_views_dataset}.OffenderStatute_latest` 
    GROUP BY 1
    )
    SELECT
        pei.person_id,
        pei.state_code,
        ofs.description,
        DispositionDate AS disposition_date,
        CAST(CAST(ArrestDate AS DATETIME) AS DATE) AS sentence_effective_date,
        CAST(CAST(OffenseDate AS DATETIME) AS DATE) AS offense_date,
        CourtName AS conviction_county,
        DocketNumber AS docket_number,
        ofs.description LIKE '%DOMESTIC%' AS is_violent_domestic,
        REGEXP_CONTAINS(ofs.description, 'DUI|INFLUENCE|DWI') AS is_dui,
        ofs.description LIKE '%13%' AND ofs.description LIKE '%VICT%' AS is_victim_under_18,
        REGEXP_CONTAINS(ofs.description, 'HOMICIDE|MURD') AS is_homicide,
        off_lk.is_sex_offense,
        off_lk.is_violent     
    FROM `{project_id}.{raw_data_up_to_date_views_dataset}.PriorRecord_latest` pr
    LEFT JOIN 
        (
        SELECT 
            *,
            UPPER(REGEXP_REPLACE(OffenseDescription, "  ", " ")) AS description,
        FROM `{project_id}.{raw_data_up_to_date_views_dataset}.OffenderStatute_latest`
        ) ofs
            ON COALESCE(pr.ConvictionOffense, pr.ChargeOffense) = ofs.Offense
    LEFT JOIN offense_flag_lk_cte off_lk
        ON off_lk.description = ofs.description
    LEFT JOIN `{project_id}.{normalized_state_dataset}.state_person_external_id` pei
        ON pr.OffenderID = pei.external_id
        AND pei.state_code = 'US_TN' 
    /* This avoids double counting offenses when surfaced on front end since prior record has ~10% entries autogenerated from TN sentences data
    and we're capturing that with Sentence and Diversion tables */
    WHERE COALESCE(DispositionText,'') NOT LIKE 'PRIOR RECORD AUTO CREATED%'
"""

US_TN_PRIOR_RECORD_PREPROCESSED_VIEW_BUILDER = SimpleBigQueryViewBuilder(
    dataset_id=ANALYST_VIEWS_DATASET,
    normalized_state_dataset=NORMALIZED_STATE_DATASET,
    view_id=US_TN_PRIOR_RECORD_PREPROCESSED_VIEW_NAME,
    description=US_TN_PRIOR_RECORD_PREPROCESSED_VIEW_DESCRIPTION,
    view_query_template=US_TN_PRIOR_RECORD_PREPROCESSED_QUERY_TEMPLATE,
    raw_data_up_to_date_views_dataset=raw_latest_views_dataset_for_region(
        state_code=StateCode.US_TN, instance=DirectIngestInstance.PRIMARY
    ),
    should_materialize=True,
)

if __name__ == "__main__":
    with local_project_id_override(GCP_PROJECT_STAGING):
        US_TN_PRIOR_RECORD_PREPROCESSED_VIEW_BUILDER.build_and_print()
