"""Script to copy autogenerated LookML files from local `pulse-data` repo to a branch in the `looker` repo.

This script is intended to help developers preview how their autogenerated LookML changes will appear in the
Looker console before merging the change into the `pulse-data` repo. These branches are not intended to be merged
and will be automatically deleted after 30 days.

 Usage:
    python -m recidiviz.tools.looker.copy_lookml_to_looker_branch \
        [--base-branch releases/v1.671-rc] \
        [--branch-name my-test-branch]

You should specify a `--branch-name` if you want to update an existing branch. Otherwise, a new branch will be created
with name autogenerated based on your git username and the current timestamp.

Note: This script uses your configured Git credentials - make sure you have access to the Recidiviz/looker
repo before running.
"""
import argparse
import logging
from datetime import datetime

from recidiviz.common.git import get_normalized_git_username
from recidiviz.tools.looker.copy_all_lookml import copy_all_autogenerated_lookml
from recidiviz.tools.looker.script_helpers import get_git_manager_for_temp_looker_repo
from recidiviz.tools.utils.git_manager import GitManager

COMMIT_MSG = "[DO NOT MERGE] Sync local autogenerated LookML changes from `pulse-data` for testing purposes"


def parse_arguments() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Copy autogenerated LookML files from local `pulse-data` repo to a branch in the `looker` repo."
    )
    parser.add_argument(
        "--branch-name",
        type=str,
        help="The name of the looker branch to create and push LookML changes to. "
        "If not provided, a branch will be autogenerated based on username and timestamp.",
    )
    parser.add_argument(
        "--base-branch",
        type=str,
        default="main",
        help="Base branch in the Looker repo to branch from (default: main).",
    )
    return parser.parse_args()


def generate_test_branch_name() -> str:
    """Generate an autogenerated branch name based on git username and timestamp."""
    username = get_normalized_git_username()
    timestamp = datetime.now().strftime("%Y%m%d-%H%M%S")
    return f"test-lookml/{username}-{timestamp}"


def copy_lookml_to_looker_branch(
    git_manager: GitManager,
    branch_name: str,
    base_branch: str,
) -> None:
    git_manager.create_branch_if_not_exists(
        base_branch_name=base_branch,
        new_branch_name=branch_name,
    )

    copy_all_autogenerated_lookml(str(git_manager.repo_root))

    if not git_manager.has_uncommitted_changes():
        logging.info("No changes detected. LookML is already up to date.")
        return

    git_manager.commit_and_push_all_changes(commit_msg=COMMIT_MSG)

    logging.info("Successfully pushed LookML changes to branch [%s]", branch_name)
    logging.info(
        "View the changes at: https://github.com/Recidiviz/looker/tree/%s", branch_name
    )


def main() -> None:
    logging.basicConfig(
        level=logging.INFO, format="%(asctime)s - %(levelname)s - %(message)s"
    )

    args = parse_arguments()
    branch_name = args.branch_name if args.branch_name else generate_test_branch_name()

    git_manager = get_git_manager_for_temp_looker_repo()

    copy_lookml_to_looker_branch(
        git_manager=git_manager,
        branch_name=branch_name,
        base_branch=args.base_branch,
    )


if __name__ == "__main__":
    main()
