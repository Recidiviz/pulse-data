"""Script to poll for Looker PR merge.

Intended to be run as a GitHub Action, this script polls the Looker repository
to check if the auto-generated PR for a given pulse-data commit has been merged.
It will timeout after a specified number of minutes.
"""
import argparse
import logging

from github import Auth, Github

from recidiviz.tools.looker.looker_sync_utils import get_autogenerated_branch_name
from recidiviz.utils.github import LOOKER_REPO_NAME, poll_for_pr_merge


def poll_for_looker_pr_merge(
    github_token: str,
    commit_sha: str,
    base_branch: str,
    timeout_minutes: int,
) -> None:
    """Poll for the Looker PR to be merged, timing out after the specified minutes."""
    github_client = Github(auth=Auth.Token(github_token))
    looker_branch_name = get_autogenerated_branch_name(commit_sha)

    try:
        poll_for_pr_merge(
            github_client=github_client,
            pr_branch=looker_branch_name,
            base_branch=base_branch,
            repo=LOOKER_REPO_NAME,
            timeout_minutes=timeout_minutes,
        )
    except TimeoutError as e:
        raise TimeoutError(
            f"{e}\n\n"
            "Please check the Looker PR for any failing workflow checks or merge conflicts. "
            "If the `LookML Validation` check is failing on the `Looker` PR, you need to fix any LookML errors "
            "and push those changes to the autogenerated `Looker` branch.\n"
            "If the issue cannot be resolved quickly, consider reverting the `pulse-data` commit since "
            "any new auto-generated Looker PRs will be blocked from being merged until this is resolved."
        ) from e


def parse_arguments() -> argparse.Namespace:
    """Parse command line arguments."""
    parser = argparse.ArgumentParser(
        description="Poll for Looker PR merge with timeout."
    )
    parser.add_argument(
        "--commit-sha",
        type=str,
        required=True,
        help="The commit SHA from pulse-data that was synced.",
    )
    parser.add_argument(
        "--base-branch",
        type=str,
        required=True,
        help="Base branch that the Looker PR targets.",
    )
    parser.add_argument(
        "--github-token",
        type=str,
        required=True,
        help="GitHub token with looker repo access.",
    )
    parser.add_argument(
        "--timeout-minutes",
        type=int,
        required=True,
        help="Maximum time to wait for PR merge in minutes.",
    )
    return parser.parse_args()


def main() -> None:
    """Main entry point for the script."""
    logging.basicConfig(
        level=logging.INFO, format="%(asctime)s - %(levelname)s - %(message)s"
    )

    args = parse_arguments()
    poll_for_looker_pr_merge(
        github_token=args.github_token,
        commit_sha=args.commit_sha,
        base_branch=args.base_branch,
        timeout_minutes=args.timeout_minutes,
    )


if __name__ == "__main__":
    main()
