"""Utility functions for Looker sync scripts."""
from recidiviz.utils.github import RECIDIVIZ_DATA_REPO, get_short_commit_sha
from recidiviz.utils.string import StrictStringFormatter

_AUTOGENERATED_BRANCH_TEMPLATE = "update-lookml-sync-{commit_sha}"
_AUTOGENERATED_MESSAGE_TEMPLATE = (
    f"[AUTOGENERATED] Automated sync from {RECIDIVIZ_DATA_REPO}@{{commit_sha}}"
)
_AUTOGENERATED_BODY_TEMPLATE = (
    "## This PR was created automatically by the `create_recidiviz_data_to_looker_sync_pr` script from "
    f"{RECIDIVIZ_DATA_REPO} commit {{commit_sha}}."
)


def get_autogenerated_branch_name(commit_sha: str) -> str:
    """Get the autogenerated branch name for a given commit SHA."""
    return StrictStringFormatter().format(
        _AUTOGENERATED_BRANCH_TEMPLATE, commit_sha=get_short_commit_sha(commit_sha)
    )


def get_autogenerated_pr_title(commit_sha: str) -> str:
    """Get the autogenerated PR title for a given commit SHA."""
    return StrictStringFormatter().format(
        _AUTOGENERATED_MESSAGE_TEMPLATE, commit_sha=get_short_commit_sha(commit_sha)
    )


def get_autogenerated_pr_body(commit_sha: str) -> str:
    """Get the autogenerated PR body for a given commit SHA."""
    return StrictStringFormatter().format(
        _AUTOGENERATED_BODY_TEMPLATE, commit_sha=commit_sha
    )


def get_autogenerated_commit_message(commit_sha: str) -> str:
    """Get the autogenerated commit message for a given commit SHA."""
    return StrictStringFormatter().format(
        _AUTOGENERATED_MESSAGE_TEMPLATE, commit_sha=commit_sha
    )
