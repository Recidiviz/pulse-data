"""Scans the codebase for TODOs referencing recently-closed Linear issues and
reopens any issues that still have outstanding TODOs.

Queries Linear for issues completed/cancelled in a configurable lookback window,
then checks the codebase at HEAD for matching TODO references. When matches are
found, the issue is transitioned back to its team's triage state and a comment
is posted listing the file locations.

Auto-generated issues from infrastructure alerting (stale raw data, dataflow
pipeline failures, raw data import failures) are excluded since they never have
corresponding TODOs in the codebase.

Usage (called by .github/workflows/reopen-closed-todo-issues.yml):
  python -m recidiviz.tools.github.reopen_closed_todo_issues \
    --linear-api-key $LINEAR_API_KEY \
    --lookback-minutes 70
"""

import argparse
import logging
import sys
from datetime import datetime, timedelta, timezone
from pathlib import Path

from recidiviz.github.github_code_reference import GithubCodeReference
from recidiviz.issue_tracking.codebase_todos import get_entire_codebase_issue_references
from recidiviz.issue_tracking.labels import (
    DATAFLOW_PIPELINE_FAILURE_LABEL,
    RAW_DATA_IMPORT_FAILURE_LABEL,
    STALE_RAW_DATA_LABEL,
)
from recidiviz.issue_tracking.linear.linear_client import LinearApiError, LinearClient
from recidiviz.issue_tracking.two_way_sync import resolve_cross_references
from recidiviz.tools.utils.git_manager import GitManager

DEFAULT_LOOKBACK_MINUTES = 70

# Labels that indicate this is an autogenerated issue that likely doesn't have TO-DOs
# in code.
EXCLUDE_LABELS = [
    STALE_RAW_DATA_LABEL,
    DATAFLOW_PIPELINE_FAILURE_LABEL,
    RAW_DATA_IMPORT_FAILURE_LABEL,
]

_WORKFLOW_URL = (
    "https://github.com/Recidiviz/pulse-data/actions/workflows/"
    "reopen-closed-todo-issues.yml"
)


def create_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Reopens Linear issues that still have TODOs in the codebase."
    )
    parser.add_argument(
        "--linear-api-key",
        required=True,
        help="API key for querying Linear.",
    )
    parser.add_argument(
        "--lookback-minutes",
        type=int,
        default=DEFAULT_LOOKBACK_MINUTES,
        help="How far back to look for completed issues (default: 70).",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Log what would be reopened without actually modifying Linear.",
    )
    return parser


def build_reopen_comment(
    code_refs: list[GithubCodeReference], *, commit_sha: str
) -> str:
    todo_list = "\n".join(
        f"* [`{ref}`]({ref.get_github_url(commit_sha)})" for ref in sorted(code_refs)
    )
    return (
        "This issue was automatically reopened because the following TODOs "
        "still reference it in the codebase:\n\n"
        f"{todo_list}\n\n"
        "Before closing this issue, please either:\n"
        "- Address the TODOs and remove them from the codebase, or\n"
        "- Re-point the TODOs to a different tracking issue.\n\n"
        f"_Posted by [reopen-closed-todo-issues]({_WORKFLOW_URL}) "
        "GitHub Action._"
    )


def main(*, linear_api_key: str, lookback_minutes: int, dry_run: bool) -> int:
    """Scans for TODOs referencing recently-closed Linear issues and reopens
    any that still have outstanding references. Returns non-zero if any issues
    failed to reopen."""
    head_sha = GitManager(repo_root=Path.cwd()).get_head_sha()

    since = datetime.now(tz=timezone.utc) - timedelta(minutes=lookback_minutes)
    linear_client = LinearClient(linear_api_key)

    logging.info(
        "Querying Linear for issues closed in the last %d minutes...",
        lookback_minutes,
    )
    closed_linear_issues = linear_client.get_recently_closed_issues(
        since, exclude_with_labels=EXCLUDE_LABELS
    )

    logging.info(
        "Found %d closed issues in the last %d minutes",
        len(closed_linear_issues),
        lookback_minutes,
    )

    logging.info("Scanning codebase for TODO references...")
    all_issue_refs = get_entire_codebase_issue_references(commit_ref=head_sha)
    logging.info("Found TODOs referencing %d distinct issues", len(all_issue_refs))

    reopened_count = 0
    failed_count = 0
    for i, closed_linear_issue in enumerate(closed_linear_issues):
        if (i + 1) % 50 == 0:
            logging.info(
                "Processing issue %d/%d...",
                i + 1,
                len(closed_linear_issues),
            )
        closed_issue_and_linked_issues = resolve_cross_references(
            {closed_linear_issue.linear_issue}, linear_client
        )
        matching_refs: list[GithubCodeReference] = []
        for closed_issue in closed_issue_and_linked_issues:
            if closed_issue not in all_issue_refs:
                continue
            matching_refs.extend(all_issue_refs[closed_issue])
        if not matching_refs:
            continue

        comment = build_reopen_comment(matching_refs, commit_sha=head_sha)

        if dry_run:
            print(
                f"\n[DRY RUN] Would reopen {closed_linear_issue.identifier} "
                f"({closed_linear_issue.title}) — "
                f"{len(matching_refs)} TODOs found"
            )
            print(comment)
            continue

        logging.info(
            "Reopening %s (%s) — %d TODOs found",
            closed_linear_issue.identifier,
            closed_linear_issue.title,
            len(matching_refs),
        )

        try:
            linear_client.reopen_issue(closed_linear_issue)
            linear_client.create_comment(closed_linear_issue, comment)
            reopened_count += 1
        except LinearApiError:
            logging.exception("Failed to reopen %s", closed_linear_issue.identifier)
            failed_count += 1

    if reopened_count:
        print(f"Reopened {reopened_count} issue(s) with outstanding TODOs")
    else:
        print("No issues needed reopening")
    if failed_count:
        print(f"Failed to reopen {failed_count} issue(s)")
        return 1

    return 0


if __name__ == "__main__":
    logging.getLogger().setLevel(logging.INFO)
    args = create_parser().parse_args()
    sys.exit(
        main(
            linear_api_key=args.linear_api_key,
            lookback_minutes=args.lookback_minutes,
            dry_run=args.dry_run,
        )
    )
