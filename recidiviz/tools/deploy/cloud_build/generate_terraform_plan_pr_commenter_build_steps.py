"""Generates the Cloud Build config YAML for the terraform-plan-pr-commenter
trigger.

The terraform-plan-pr-commenter trigger runs `terraform plan` on pull
requests and posts the result as a PR comment. Its config is consumed by the
Terraform resource in terraform-plan-pr-commenter.tf via `yamldecode`, so the
trigger config stays in sync with the Python `CreateTerraformPlan` deploy
stage.

Run this script whenever create_terraform_plan.py or related constants
change, then commit the updated YAML file:

    python -m recidiviz.tools.deploy.cloud_build.generate_terraform_plan_pr_commenter_build_steps
"""
import argparse
import os

import yaml
from google.cloud.devtools.cloudbuild_v1 import BuildOptions

import recidiviz
from recidiviz.tools.deploy.cloud_build.build_configuration import (
    DeploymentContext,
    build_config_to_dict,
    strip_build_config_defaults,
)
from recidiviz.tools.deploy.cloud_build.stages.create_terraform_plan import (
    CreateTerraformPlan,
)

# Placeholders resolved at build execution time via trigger-level
# substitutions (which come from the webhook body) and build-level
# substitutions (hardcoded on the trigger).
_DEPLOYMENT_CONTEXT = DeploymentContext(
    project_id="$_PROJECT_ID",
    commit_ref="$_COMMIT_REF",
    version_tag="$_VERSION_TAG",
    stage="CreateTerraformPlan",
)

# Build-level service account for the trigger. The trigger-level SA (who
# creates the build) is set on the Terraform resource itself.
_BUILD_SERVICE_ACCOUNT = (
    "projects/$_PROJECT_ID/serviceAccounts/"
    "cloud-build-ci-cd@$_PROJECT_ID.iam.gserviceaccount.com"
)

OUTPUT_PATH = os.path.join(
    os.path.dirname(recidiviz.__file__),
    "tools/deploy/terraform/config/terraform_plan_pr_commenter_build_steps.yaml",
)


def generate() -> dict:
    """Generate the PR-commenter trigger build config. Returns the full
    Cloud Build configuration dict with default and response-only fields
    stripped."""
    build_configuration = CreateTerraformPlan().configure_build(
        deployment_context=_DEPLOYMENT_CONTEXT,
        args=argparse.Namespace(apply=False, for_pull_requests=True),
    )
    config_dict = build_config_to_dict(
        build_configuration=build_configuration,
        deployment_context=_DEPLOYMENT_CONTEXT,
        service_account=_BUILD_SERVICE_ACCOUNT,
    )
    config_dict = strip_build_config_defaults(config_dict)
    config_dict["options"]["machine_type"] = BuildOptions.MachineType(
        config_dict["options"]["machine_type"]
    ).name
    return config_dict


_HEADER_COMMENT = (
    "# Auto-generated by generate_terraform_plan_pr_commenter_build_steps.py.\n"
    "# Do not edit manually. To regenerate, run:\n"
    "#   python -m recidiviz.tools.deploy.cloud_build"
    ".generate_terraform_plan_pr_commenter_build_steps\n"
)


class _PrettierYamlDumper(yaml.Dumper):
    """YAML dumper that indents list items under their parent key, matching
    prettier's formatting expectations."""

    def increase_indent(self, flow: bool = False, indentless: bool = False) -> None:
        return super().increase_indent(flow, indentless=False)


if __name__ == "__main__":
    output = generate()
    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        f.write(_HEADER_COMMENT)
        yaml.dump(
            output,
            f,
            Dumper=_PrettierYamlDumper,
            default_flow_style=False,
            sort_keys=False,
        )
    print(f"Wrote {OUTPUT_PATH}")
